function checknumber_binary(num){
    if (num == "") {
        return true;
    } else {
        var check_regex = /^[0-9]+(\.\d{1})?$/g;
        if (check_regex.test(num)) {
            return 1;
        } else {
            return 0;
        }
    }
    
}
